#!/usr/bin/env python3
""" a module to create a function to concatenate two strings """

def concat(str1: str, str2: str) -> str:
    """ a function to concatenate two strings and return a string
    Args:
        str1: the first string
        str2: the second string
    Return: the first and second string added toghether
    """

    return str1 + str2